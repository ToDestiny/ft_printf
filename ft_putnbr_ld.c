#include "ft_printf.h"

void       ft_putnbr_ld(long n)
{
    if (n == -9223372036854775807)
        ft_putstr("-9223372036854775807");
    else if (n < 0)
    {
        n *= -1;
        ft_putchar('-');
        ft_putnbr_ld(n);
    }
    else if (n >= 10)
    {
        ft_putnbr_ld(n / 10);
        ft_putnbr_ld(n % 10);
    }
    else
        ft_putchar(48 + n);
}